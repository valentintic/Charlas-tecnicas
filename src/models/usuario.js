export class Usuario {
    constructor(
        idUsuario, nombre, apellidos, email, estadoUsuario, imagen, password, idRole
    ){
        this.idUsuario = idUsuario,
        this.nombre = nombre,
        this.apellidos = apellidos,
        this.email = email,
        this.estadoUsuario = estadoUsuario,
        this.imagen = imagen,
        this.password = password,
        this.idRole = idRole
    }
}

